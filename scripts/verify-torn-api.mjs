#!/usr/bin/env node
// Local-only Torn API verification script.
//
// Reads TORN_API_KEY from .env.local, tests each `/user/` selection this app
// depends on individually, and writes a results table to
// docs/API_VERIFICATION_RESULTS.md. The key is read into memory only — it is
// never logged, printed, or written to the results file.
//
// Usage: npm run verify:torn-api

import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");
const ENV_PATH = join(ROOT, ".env.local");
const OUTPUT_PATH = join(ROOT, "docs", "API_VERIFICATION_RESULTS.md");

// Selection names reflect the *current* Torn API. `stats`/`weapons`/`armor`
// were renamed to `battlestats`/`equipment`; `chain` is faction-scoped (not
// exposed via `/user/` to a personal key); `enlistedcars` requires the v2
// API (the v1 `/user/` endpoint returns "code 23: only available in v2").
const SELECTIONS = [
  "basic",
  "profile",
  "bars",
  "battlestats",
  "travel",
  "networth",
  "inventory",
  "properties",
  "equipment",
  "crimes",
  "cooldowns",
];

const V2_SELECTIONS = ["enlistedcars"];

function loadEnvLocal(path) {
  const raw = readFileSync(path, "utf8");
  const env = {};
  for (const line of raw.split("\n")) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#")) continue;
    const eq = trimmed.indexOf("=");
    if (eq === -1) continue;
    const key = trimmed.slice(0, eq).trim();
    let value = trimmed.slice(eq + 1).trim();
    if (
      (value.startsWith('"') && value.endsWith('"')) ||
      (value.startsWith("'") && value.endsWith("'"))
    ) {
      value = value.slice(1, -1);
    }
    env[key] = value;
  }
  return env;
}

async function testSelection(key, selection) {
  const url = `https://api.torn.com/user/?selections=${encodeURIComponent(selection)}&key=${encodeURIComponent(key)}`;

  let json;
  try {
    const response = await fetch(url);
    if (!response.ok) {
      return {
        selection,
        ok: false,
        code: null,
        message: `HTTP ${response.status}`,
        fields: [],
      };
    }
    json = await response.json();
  } catch (error) {
    return {
      selection,
      ok: false,
      code: null,
      message: `Request error: ${error.message}`,
      fields: [],
    };
  }

  if (json.error) {
    return {
      selection,
      ok: false,
      code: json.error.code,
      message: json.error.error,
      fields: [],
    };
  }

  return {
    selection,
    ok: true,
    code: null,
    message: "",
    fields: Object.keys(json),
  };
}

async function testV2Selection(key, selection) {
  const url = `https://api.torn.com/v2/user/${encodeURIComponent(selection)}?key=${encodeURIComponent(key)}`;

  let json;
  try {
    const response = await fetch(url);
    if (!response.ok) {
      return {
        selection: `${selection} (v2)`,
        ok: false,
        code: null,
        message: `HTTP ${response.status}`,
        fields: [],
      };
    }
    json = await response.json();
  } catch (error) {
    return {
      selection: `${selection} (v2)`,
      ok: false,
      code: null,
      message: `Request error: ${error.message}`,
      fields: [],
    };
  }

  if (json.error) {
    return {
      selection: `${selection} (v2)`,
      ok: false,
      code: json.error.code,
      message: json.error.error,
      fields: [],
    };
  }

  return {
    selection: `${selection} (v2)`,
    ok: true,
    code: null,
    message: "",
    fields: Object.keys(json),
  };
}

function renderMarkdown(results, testedAt) {
  const lines = [];
  lines.push("# Torn API verification results");
  lines.push("");
  lines.push(
    `Generated by \`npm run verify:torn-api\` on ${testedAt}. This file is regenerated each` +
      " run — do not hand-edit it. The key used to produce these results is read from" +
      " `.env.local` and is never written here.",
  );
  lines.push("");
  lines.push("| Selection | Result | Error code | Message | Top-level fields returned |");
  lines.push("|---|---|---|---|---|");

  for (const result of results) {
    const status = result.ok ? "✅ success" : "❌ fail";
    const code = result.code ?? "—";
    const message = result.ok ? "—" : result.message || "—";
    const fields = result.ok ? result.fields.join(", ") || "—" : "—";
    lines.push(`| \`${result.selection}\` | ${status} | ${code} | ${message} | ${fields} |`);
  }

  lines.push("");
  const succeeded = results.filter((r) => r.ok).map((r) => `\`${r.selection}\``);
  const failed = results.filter((r) => !r.ok).map((r) => `\`${r.selection}\``);
  lines.push(`**Succeeded (${succeeded.length}):** ${succeeded.join(", ") || "none"}`);
  lines.push("");
  lines.push(`**Failed (${failed.length}):** ${failed.join(", ") || "none"}`);
  lines.push("");

  return lines.join("\n");
}

async function main() {
  let env;
  try {
    env = loadEnvLocal(ENV_PATH);
  } catch {
    console.error(`Could not read ${ENV_PATH}. Create it with a TORN_API_KEY entry first.`);
    process.exitCode = 1;
    return;
  }

  const key = env.TORN_API_KEY;
  if (!key) {
    console.error("TORN_API_KEY is not set in .env.local.");
    process.exitCode = 1;
    return;
  }

  const total = SELECTIONS.length + V2_SELECTIONS.length;
  console.log(`Testing ${total} Torn API selections individually (key not shown)...`);

  const results = [];
  for (const selection of SELECTIONS) {
    const result = await testSelection(key, selection);
    results.push(result);
    const status = result.ok ? "OK" : `FAIL (code ${result.code ?? "?"}: ${result.message})`;
    console.log(`  ${selection.padEnd(18)} ${status}`);
  }
  for (const selection of V2_SELECTIONS) {
    const result = await testV2Selection(key, selection);
    results.push(result);
    const status = result.ok ? "OK" : `FAIL (code ${result.code ?? "?"}: ${result.message})`;
    console.log(`  ${result.selection.padEnd(18)} ${status}`);
  }

  const testedAt = new Date().toISOString();
  const markdown = renderMarkdown(results, testedAt);
  writeFileSync(OUTPUT_PATH, markdown, "utf8");

  console.log(`\nResults written to ${OUTPUT_PATH}`);
}

main();
